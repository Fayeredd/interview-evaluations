package com.revature.security.extra;

/**
 *
 * @author FayeRedd
 */
import javax.persistence.*;
import javax.validation.constraints.NotNull;
import java.util.List;

/*
This is a placeholder Entity for an "Authority" POJO. Feel free to modify/edit as needed
for compatibility with actual database Table
*/

@Entity
@Table(name = "ie_AUTHORITY")
public class Authority {

    @Id
    @Column(name = "a_authID")
    @GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "authority_seq")
    @SequenceGenerator(name = "authority_seq", sequenceName = "authority_seq", allocationSize = 1)
    private Long id;

    @Column(name = "a_authorityName", length = 50)
    @NotNull
    @Enumerated(EnumType.STRING)
    private AuthorityName name;

    @ManyToMany(mappedBy = "authorities", fetch = FetchType.LAZY)
    private List<User> users;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public AuthorityName getName() {
        return name;
    }

    public void setName(AuthorityName name) {
        this.name = name;
    }

    public List<User> getUsers() {
        return users;
    }

    public void setUsers(List<User> users) {
        this.users = users;
    }
}